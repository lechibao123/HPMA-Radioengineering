% --------------------------------------------
%  m,n / A |   \
% G   |    | x  |,
%  p,q \ B |   /
% --------------------------------------------
% A = [a1,a2,...,ap]
% B = [b1,b2,...,bq]
% --------------------------------------------
function result = newMeijerG(m,n,p,q,A,B,x)
    An = A(1:n); Ap = A(n+1:p);
    Bm = B(1:m); Bq = B(m+1:q);

    result = meijerG([An],[Ap],[Bm],[Bq],x);
end